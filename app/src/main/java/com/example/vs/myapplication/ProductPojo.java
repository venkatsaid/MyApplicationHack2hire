package com.example.vs.myapplication;

public class ProductPojo {
    String productId;
    String productName;
    String productCategory;
    String productSpecification;
    String productPrice;
    String productImageURL;
    String productSearchTextURL;
    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductCategory() {
        return productCategory;
    }

    public void setProductCategory(String productCategory) {
        this.productCategory = productCategory;
    }

    public String getProductSpecification() {
        return productSpecification;
    }

    public void setProductSpecification(String productSpecification) {
        this.productSpecification = productSpecification;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductImageURL() {
        return productImageURL;
    }

    public void setProductImageURL(String productImageURL) {
        this.productImageURL = productImageURL;
    }

    public String getProductSearchTextURL() {
        return productSearchTextURL;
    }

    public void setProductSearchTextURL(String productSearchTextURL) {
        this.productSearchTextURL = productSearchTextURL;
    }


}
